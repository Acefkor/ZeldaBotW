public class Titular {

    private String titulo;
    private Integer img;

    public Titular(String titulo, Integer img) {
        this.titulo = titulo;
        this.img = img;
    }

    public String getTitulo() {
        return titulo;
    }

    public Integer getImg() {
        return img;
    }
}
